            <h3>A CULTURA DOS CORDÉIS: MAPEAMENTO TEMÁTICO E TERRITORIAL NO BRASIL</h3>
              <p>
A geração de conhecimento e de conceitos em uma determinada área temática, especificamente, na literatura popular, estimula pesquisadores a ensejar mecanismos e instrumentos eficazes para a recuperação, preservação disseminação e acesso às informações. O processo de armazenamento, busca, recuperação e disseminação de informação tem-se alterado significativamente, em pouco tempo, determinando uma interação direta e crescente entre os usuários e os sistemas de informação.              </p>
              <p>
Reconhecendo a importância da Literatura de Cordel, não apenas no Nordeste, berço da Literatura de Cordel no Brasil, optamos pela continuidade de estudos deste tipo de literatura. Assim, o estudo reside, essencialmente, em mapear tematicamente o território da Literatura de Cordel no Brasil, visando à continuidade de estudos temáticos para à expansão da classe de Literatura nas Classificações Bibliográficas, iniciados na tese de doutorado do Programa de Pós-graduação em Letras da Universidade Federal da Paraíba.               </p>
              <p>
Nesse sentido, buscamos um estágio pós-doutoral, cujo objeto de estudo contribua para a Ciência da Informação, especificamente para a área da Representação Temática da Informação. Assim, motivada por este interesse, encontramos no Programa de Pós-Graduação em Ciência da Informação da Escola de Comunicação da Universidade de São Paulo a área de concentração “Cultura e informação”, apontando alternativas conceituais, teóricas e metodológicas, para a linha de pesquisa “Organização da Informação e do Conhecimento”.              </p>
        
